public class ConfusedTurtle extends Turtle
{

  // constructor
  public ConfusedTurtle(World theWorld)
  {
    super(theWorld);
  }

  public void turnLeft()
  {
    super.turnLeft();
  }
  
  public void turnRight()
  {
    super.turnLeft();
  }
  
} // this is the end of the class ConfusedTurtle